package com.syntax.class2;

public class IncrementOperator {

	public static void main(String[] args) {
		int number=10;
		System.out.println(number);
		number=number+1;
		number=number+number;
		System.out.println(number);
		System.out.println("********************");
		number++; //increments the value by one
		System.out.println(number);
		System.out.println("************");
		number =10;
		
		

	}

}
