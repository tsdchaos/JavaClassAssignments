package com.syntax.class7;

public class doWhilePractice {

	public static void main(String[] args) {

		int num1=30;
		int num2=99;
		do {
			System.out.print(num1+" ");
			num1++;
		}while(num1<=70);
		System.out.println();
		do {
			System.out.print(num2+" ");
			num2--;
		}while(num2>=1);

	}

}
